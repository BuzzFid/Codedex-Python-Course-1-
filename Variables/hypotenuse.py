
# Phythagorean theorem

# The (a) is the length of a short side.
# The (b) is the length of another short side.
# The (c) is the length of the hypotenuse.

a = int(input("Enter the length of the first short side: "))
b = int(input("Enter the length of the second short side: "))

c=(a**2+b**2)**0.5

print("The length of the hypotenuse is", c)